#include <unistd.h>
#include <stdio.h>

void	ft_write(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char *argv[])
{
	int	i;

	i = 0;
	while (argv[0][i])
	{
		ft_write(argv[0][i]);
		i++;
	}
	write(1, "\n", 1);
}
